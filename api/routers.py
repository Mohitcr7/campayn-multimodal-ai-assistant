@feedback_router.post(
    "/",
    response_model = FeedbackResponse,
    summary        = "Log user feedback",
    description    = "Record thumbs up/down on a response. Stored in LangSmith for eval.",
)
async def log_feedback(request: FeedbackRequest) -> FeedbackResponse:
    """
    POST /feedback

    Call this when the user clicks thumbs up or down on a response.
    The run_id comes from the ChatResponse of the rated message.

    Body:
        {"run_id": "abc123", "score": 1.0, "comment": "Very helpful!"}
    """
    from api.observability.langsmith_setup import log_feedback as _log

    success = _log(
        run_id  = request.run_id,
        score   = request.score,
        comment = request.comment or "",
    )

    return FeedbackResponse(
        success = success,
        message = "Feedback logged" if success else "Feedback logging failed (LangSmith may be disabled)",
    )


