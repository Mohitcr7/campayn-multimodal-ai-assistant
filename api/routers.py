# ─────────────────────────────────────────────────────────────────
# SHARED HELPER — run the pipeline and build the response
# Both /brand/chat and /creator/chat use this same logic.
# The only difference is the role passed in.
# ─────────────────────────────────────────────────────────────────

async def _run_chat(request: ChatRequest, role: str) -> ChatResponse:
    """
    Core handler used by both brand and creator endpoints.

    Flow:
      1. Generate session_id if not provided
      2. Call run_pipeline() from graph.py
      3. Compute metrics
      4. Return structured ChatResponse
    """
    from api.graph                       import run_pipeline
    from api.observability.langsmith_setup import compute_pipeline_metrics, build_run_metadata

    # Generate session ID on first message
    session_id = request.session_id or str(uuid.uuid4())

    try:
        # ── Run the full LangGraph pipeline ──────────────────────────────
        result = run_pipeline(
            query       = request.query,
            role        = role,
            session_id  = session_id,
            image_b64   = request.image_b64,
            mcq_answers = request.mcq_answers,
        )

        # ── Build metrics ─────────────────────────────────────────────────
        metrics_data = compute_pipeline_metrics(result)
        metrics = PipelineMetrics(**{
            k: v for k, v in metrics_data.items()
            if k in PipelineMetrics.model_fields
        })

        # ── Handle MCQ interrupt ──────────────────────────────────────────
        if result.get("needs_mcq"):
            raw_questions = result.get("mcq_questions") or []
            mcq_questions = [
                MCQQuestion(
                    field   = q.get("field", ""),
                    label   = q.get("label", ""),
                    options = q.get("options", []),
                    hint    = q.get("hint", ""),
                )
                for q in raw_questions
            ]
            return ChatResponse(
                response      = result.get("response", ""),
                session_id    = session_id,
                role          = role,
                needs_mcq     = True,
                mcq_questions = mcq_questions,
                metrics       = metrics,
            )

        # ── Normal response ───────────────────────────────────────────────
        meta = result.get("metadata", {})
        return ChatResponse(
            response   = result.get("response", ""),
            session_id = session_id,
            role       = role,
            needs_mcq  = False,
            sources    = meta.get("sources", []),
            run_id     = result.get("run_id"),   # LangSmith run ID for feedback
            metrics    = metrics,
        )

    except Exception as e:
        # Return a structured error instead of crashing
        # In production: log to LangSmith + alert
        print(f"[API] Pipeline error (role={role}): {e}")
        raise HTTPException(
            status_code = 500,
            detail      = f"Pipeline error: {str(e)[:200]}",
        )


