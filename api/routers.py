@health_router.get(
    "/health",
    response_model = HealthResponse,
    summary        = "Health check",
    description    = "Check API status and module availability.",
)
async def health_check() -> HealthResponse:
    """
    GET /health

    Returns the status of all modules:
      - chromadb: is the vector store loaded?
      - dataframe: are the 56k profiles in memory?
      - langsmith: is tracing enabled?
    """
    import os

    modules = {}

    # Check ChromaDB
    try:
        import chromadb
        persist_dir = os.environ.get("CHROMA_PERSIST_DIR", "data/vector_store")
        client = chromadb.PersistentClient(path=persist_dir)
        col = client.get_collection(
            os.environ.get("CHROMA_COLLECTION_NAME", "campayn_knowledge_base")
        )
        modules["chromadb"] = f"ok ({col.count()} chunks)"
    except Exception as e:
        modules["chromadb"] = f"error: {str(e)[:60]}"

    # Check DataFrame
    try:
        from api.graph import get_dataframe
        df = get_dataframe()
        modules["creator_profiles"] = f"ok ({len(df):,} profiles)"
    except Exception as e:
        modules["creator_profiles"] = f"error: {str(e)[:60]}"

    # Check LangSmith
    langsmith_on = (
        os.environ.get("LANGCHAIN_TRACING_V2", "false").lower() == "true"
        and bool(os.environ.get("LANGCHAIN_API_KEY"))
    )
    modules["langsmith"] = "enabled" if langsmith_on else "disabled"

    # Check Gemini
    gemini_key = os.environ.get("GEMINI_API_KEY", "")
    modules["gemini"] = "configured" if gemini_key else "missing GEMINI_API_KEY"

    # Check Anthropic (automatic fallback if Gemini fails — see api/llm_fallback.py)
    anthropic_key = os.environ.get("ANTHROPIC_API_KEY", "")
    modules["anthropic_fallback"] = "configured" if anthropic_key else "not configured"

    return HealthResponse(
        status       = "ok",
        environment  = os.environ.get("ENVIRONMENT", "development"),
        langsmith_on = langsmith_on,
        modules      = modules,
    )